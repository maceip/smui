// Complete theme presets — each swaps the entire CSS variable set.
// All color values are raw HSL triplets: "H S% L%"
// Base vars (--background etc.) get wrapped in hsl() when applied.
// Extended vars (--smui-*) stay as raw triplets (they're wrapped at usage).

export interface ThemePreset {
  id: string;
  name: string;
  /** Preview swatch color (hex) */
  swatch: string;
  /** CSS variables for dark mode */
  vars: Record<string, string>;
}

