/** Apply a theme preset by setting all CSS variables on :root */
export function applyThemePreset(preset: ThemePreset) {
  const root = document.documentElement;
  const vars = preset.vars;

  for (const [key, value] of Object.entries(vars)) {
    if (key.startsWith("smui-")) {
      // Extended vars: raw HSL triplet (used as hsl(var(--smui-*)))
      root.style.setProperty(`--${key}`, value);
    } else {
      // Base vars: wrap in hsl()
      root.style.setProperty(`--${key}`, `hsl(${value})`);
    }
  }
}

/** Clear all inline styles to revert to CSS-defined theme */
export function clearThemePreset() {
  const root = document.documentElement;
  // Remove all inline style properties we may have set
  const allKeys = Object.keys(NORD.vars);
  for (const key of allKeys) {
    root.style.removeProperty(`--${key}`);
  }
}
